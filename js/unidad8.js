function mostrarTextoJs() {    
    var caja = document.getElementById('servicio_text_js')
    // el elemento no se carga
    // caja.style.display = 'none'
    if(caja.style.display == 'none'){
        caja.style.display = 'block'
    } else {
        caja.style.display = 'none' 
    }
}  

